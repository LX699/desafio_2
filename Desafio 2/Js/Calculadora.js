class Calculadora {
    
    sumar(num_1, num_2) {
        return num_1 + num_2;
    }

    restar(num_1, num_2) {
        return num_1 - num_2;
    }

    dividir(num_1, num_2) {
        return num_1 / num_2;
    }

    multiplicar(num_1, num_2) {
        return num_1 * num_2;
    }
} 